	ret += lc_compare(compare1 + LC_KMAC256_DRNG_KEYSIZE, exp1,
			  sizeof(exp1), "KMAC DRNG verification");
	lc_rng_zero(kmac_ctx);
	lc_kmac_zero(kmac_compare);

	return ret;
